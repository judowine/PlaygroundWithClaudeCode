/**
 * TodoApp Material 3 theme with support for light and dark themes.
 * Provides proper color schemes and typography for the TODO application.
 *
 * @param darkTheme Whether to use dark theme colors
 * @param content The content to theme
 */
@Composable
fun TodoAppTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {
    val colorScheme = if (darkTheme) DarkColorScheme else LightColorScheme

    MaterialTheme(
        colorScheme = colorScheme,
        typography = MaterialTheme.typography,
        content = content
    )
}